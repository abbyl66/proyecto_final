package com.example.epubook.modelo;

public class Coleccion {

    private String nombre;

    public Coleccion() {
    }

    public Coleccion(String coleccion) {
        this.nombre = coleccion;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String coleccion) {
        this.nombre = coleccion;
    }

}
